    private void showRecepId() {
          
        try
        {  jcEmpId.removeAllItems();
           recepList=ReceptionistDAO.getNonRegisteredReceptionist();
           if(recepList.isEmpty())
           {
             JOptionPane.showMessageDialog(null,"No record of non registered receptionist present","Error!",JOptionPane.ERROR_MESSAGE);
             btnAdd.setEnabled(false);
             return;
           }
           btnAdd.setEnabled(true);
           Set<String> s=recepList.keySet();
           Iterator<String> it=s.iterator();
           while(it.hasNext())
           {
             jcEmpId.addItem(it.next());
           }
        }
        catch(SQLException ex)
        {
           JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE);    
             ex.printStackTrace();
        }
    }

    private int validateInput() {
       
       if(txtPassword.getPassword().length<4||txtRePassword.getPassword().length<4)
          return 0;
       if(txtUserId.getText().trim().isEmpty())
           return -1;
       return 1;
    }

    private boolean checkPassword() {
        String password=String.valueOf(txtPassword.getPassword()).trim();
        String rePass=String.valueOf(txtRePassword.getPassword()).trim();
        return password.equals(rePass);
    }

    private void clearText() {
         txtPassword.setText("");
         txtRePassword.setText("");
         txtEmpName.setText("");
         txtUserId.setText("");
    }
