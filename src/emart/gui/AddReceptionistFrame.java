    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int x=validateInput();
        if(x==0)
        {
            JOptionPane.showMessageDialog(null, "Passwords must be atleast 4 characters long");
            return;
            
        }
        if(x==-1)
        {
            JOptionPane.showMessageDialog(null, "UserId cannot be left blank");
            return;
        }
        if(!checkPassword())
        {
            JOptionPane.showMessageDialog(null, "Passwords not matched");
            return;
        }
        try
        {
          String userId=txtUserId.getText().trim();
          if(!ReceptionistDAO.userIdAvailable(userId))
          {
            JOptionPane.showMessageDialog(null,"User Id: "+userId+" is already used !","Error",JOptionPane.INFORMATION_MESSAGE);
             txtUserId.setText("");
             txtUserId.requestFocus();
            return;
          }
          UserPojo p=new UserPojo();
          p.setEmpId(jcEmpId.getSelectedItem().toString());
          p.setUserName(txtEmpName.getText().trim());
          p.setUserType("Receptionist");
          p.setPassword(String.valueOf(txtPassword.getPassword()).trim());
          p.setUserId(userId);
          boolean res=ReceptionistDAO.addReceptionist(p);
          if(res==false)
          {
             JOptionPane.showMessageDialog(null,"Record not added","Try again",JOptionPane.ERROR_MESSAGE);
             return;
          }
          JOptionPane.showMessageDialog(null,"Record added successfully","Success!",JOptionPane.INFORMATION_MESSAGE);
          clearText();
          showRecepId();
        }
         catch(SQLException ex)
        {
           JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE);    
             ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

