    private void showRecepId() {
        try
        {
            
           recepList =ReceptionistDAO.getReceptionist();
           if(recepList.isEmpty())
           {
               JOptionPane.showMessageDialog(null,"No registered receptionist present ","Error!",JOptionPane.ERROR_MESSAGE);  
               return;
           }
           btnUpdate.setEnabled(true);
           Set<String> s=recepList.keySet();
           for(String id:s)
           {
             jcReceptionistId.addItem(id);
           }
        }
        catch(Exception ex)
        {
           JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE);    
             ex.printStackTrace();
        }
    }
    
      private boolean validateInput() {
        password=String.valueOf(txtPassword.getPassword()).trim();
        String rePass=String.valueOf(txtRePassword.getPassword()).trim();
        if(txtPassword.getPassword().length<4||txtRePassword.getPassword().length<4)
        {
          JOptionPane.showMessageDialog(null, "Passwords must be atleast 4 characters long");
          return false;
        }
        if(!password.equals(rePass))
        {
           JOptionPane.showMessageDialog(null,"Password not match!","Error",JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        return true;
    }

