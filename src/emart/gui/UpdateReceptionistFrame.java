    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInput())
         {
            return;
         }
          String userId=jcReceptionistId.getSelectedItem().toString();
          try
          {    
            boolean res =ReceptionistDAO.updateReceptionist(userId,password);
            if(!res)
            {
             JOptionPane.showMessageDialog(null,"Record not updated","Try again",JOptionPane.ERROR_MESSAGE);
             return;
           }
           
           JOptionPane.showMessageDialog(null,"Record updated successfully","Success!",JOptionPane.INFORMATION_MESSAGE);
         }
         catch(SQLException ex)
         {
             JOptionPane.showMessageDialog(null,"DB Error","Error!",JOptionPane.ERROR_MESSAGE);    
             ex.printStackTrace();
         }  
          
    }//GEN-LAST:event_btnUpdateActionPerformed

